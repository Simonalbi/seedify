/**
 * Show the edit credit card overlay.
 */
function showEditCreditCard() {
    const editCreditCardOverlay = document.getElementById("edit-credit-card");
    editCreditCardOverlay.style.visibility = "visible";
}

/**
 * Hide the edit credit card overlay.
 */
function hideEditCreditCard() {
    const editCreditCardOverlay = document.getElementById("edit-credit-card");
    editCreditCardOverlay.style.visibility = "hidden";
}
